    private void limpiarCampos() {
        this.CampoCodigo.setText(null);
        this.CampoDescripcion.setText(null);
        this.CampoPeso.setText(null);
    }
    
    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        try {
            if (controlador.verificarCodigo(this.CampoCodigo.getText())) {
                controlador.modificarProducto(new Producto(this.CampoCodigo.getText(), this.CampoDescripcion.getText(),
                        0, controlador.recuperarProducto(this.CampoCodigo.getText()).getPrecio(), 
                        controlador.recuperarProducto(this.CampoCodigo.getText()).getTipo(),
                        Float.parseFloat(this.CampoPeso.getText()), "Fresco"));
                limpiarCampos();
            } else {
                Object[] message = {"Codigo no valido para actualizar"};
                JOptionPane.showMessageDialog(ModificarProducto2.this, message, "Error", JOptionPane.OK_OPTION);
                limpiarCampos();
            }
        } catch (SQLException | NumberFormatException ex) {
            Object[] message = {"C贸digo ya existe u ocurri贸 error al ingresar un dato"};
            JOptionPane.showMessageDialog(ModificarProducto2.this, message, "Error", JOptionPane.OK_OPTION);
            limpiarCampos();
        }
    }//GEN-LAST:event_ModificarActionPerformed

